--  DDL for Trigger BIU_MAX_RESERVAS
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "PROGRAMACION"."BIU_MAX_RESERVAS" 
BEFORE INSERT OR UPDATE ON PROGRAMACION.RESERVAS
FOR EACH ROW
DECLARE
    v_total_reservas NUMBER;
BEGIN
    -- Validar solo cuando se inserta o actualiza a estado 'RESERVADA'
    IF :NEW.estado = 'RESERVADA' THEN
        SELECT COUNT(*)
        INTO v_total_reservas
        FROM PROGRAMACION.RESERVAS
        WHERE id_usuario = :NEW.id_usuario
          AND estado = 'RESERVADA';

        -- Si ya tiene 4 o m硬, lanzar excepci蚤
        IF v_total_reservas >= 4 THEN
            RAISE_APPLICATION_ERROR(-20001, 'El usuario ya tiene el m經imo de reservas activas permitidas (4).');
        END IF;
    END IF;
END;

/
ALTER TRIGGER "PROGRAMACION"."BIU_MAX_RESERVAS" ENABLE;
